contract = web3.eth.contract(address=contract_address, abi=contract_abi)


def read_smart_contract_value(function_name, args):
    # Get the contract function
    contract_function = contract.functions[function_name](*args)

    # Call the function to get the value
    value = contract_function.call()

    return value

def call_smart_contract_function(function_name, args, from_address, private_key):
    # Get the contract function
    contract_function = contract.functions[function_name](*args)

    # Build the transaction
    transaction = contract_function.build_transaction({
        'from': from_address,
        'nonce': web3.eth.get_transaction_count(from_address),
		'gas': 2000000,
        'gasPrice': 4000000000000
    })

    # Sign the transaction
    signed_txn = web3.eth.account.sign_transaction(transaction, private_key)

    # Send the transaction
    txn_hash = web3.eth.send_raw_transaction(signed_txn.rawTransaction)

    # Wait for the transaction to be mined
    txn_receipt = web3.eth.wait_for_transaction_receipt(txn_hash)
    return txn_receipt["transactionHash"].hex()

